// Operator Aritmatika 
var a = 50;
var b = 25;

// menggunakan operator penjumlahan
var c = a + b;
console.log(c);

// menggunakan operator pengurangan 
var d = a - b;
console.log(d);

// menggunakan operator perkalian 
var e = a * b;
console.log(e);

// Operator Logika AND 
true && true; // true
true && false; // false
false && true; // false
false && false; // false

2 < 3 && 6 < 8; // true && true = true
2 < 3 && 6 > 8; // true && false = false
2 > 3 && 6 < 8; // false && true = false
2 > 3 && 6 > 8; // false && false = false